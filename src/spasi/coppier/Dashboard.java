    private void btn_uploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_uploadActionPerformed
        // TODO add your handling code here:
        String userhome = System.getProperty("user.home");        
        JFileChooser jFileChooser = new JFileChooser(userhome);
        jFileChooser.setCurrentDirectory(new java.io.File("."));
        jFileChooser.setDialogTitle("Pilih Folder File");
        jFileChooser.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        jFileChooser.setMultiSelectionEnabled(true);
        int result = jFileChooser.showOpenDialog(new JFrame());
        if (result == JFileChooser.APPROVE_OPTION) {
            File[] files = jFileChooser.getSelectedFiles();
            for(File f : files) {
               String path = jFileChooser.getCurrentDirectory()+(!f.getName().isEmpty() ? "\\"+f.getName() : "");
               boolean stts = false;
               for(String line : txt_source.getText().split("\\n")) {
                   if(path.equals(line)){
                       stts = true;
                       break;
                   }
               }
               
               if(!stts) {
                   txt_source.append(path+"\n");
               }
            }
	}
    }//GEN-LAST:event_btn_uploadActionPerformed

    private void btn_destinasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_destinasiActionPerformed
        String userhome = System.getProperty("user.home");        
        JFileChooser jFileChooser = new JFileChooser(userhome);
        jFileChooser.setCurrentDirectory(new java.io.File("."));
        jFileChooser.setDialogTitle("Pilih Folder Tujuan");
        jFileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        jFileChooser.setMultiSelectionEnabled(true);
        int result = jFileChooser.showOpenDialog(new JFrame());
        if (result == JFileChooser.APPROVE_OPTION) {
            txt_destination.setText(jFileChooser.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_btn_destinasiActionPerformed

