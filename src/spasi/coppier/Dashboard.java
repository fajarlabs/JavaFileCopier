    private void btn_copyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_copyMouseClicked
        if(txt_destination.getText().isEmpty()) {
            String message = "Maaf, kolom input file yang diupload atau folder tujuan belum dilengkapi.";
            JOptionPane.showMessageDialog(new JFrame(), message, "Error",
                JOptionPane.ERROR_MESSAGE);
        } else { 
            JDialog d = new JDialog();
            PanelDialogProses panel_dialog = new PanelDialogProses(txt_source, txt_destination, d);
            d.add(panel_dialog);
            d.setTitle("Information");
            d.setSize(507, 320);
            final Toolkit toolkit = Toolkit.getDefaultToolkit();
            final Dimension screenSize = toolkit.getScreenSize();
            final int x = (screenSize.width - d.getWidth()) / 2;
            final int y = (screenSize.height - d.getHeight()) / 2;
            d.setLocation(x, y);
            d.setVisible(true);
            d.setResizable(false);
        }
    }//GEN-LAST:event_btn_copyMouseClicked

